public class main {

    /* Convert from feet to meters */
    public static double footToMeter(double foot) {
        double meter;
        meter = (0.305 * foot);
        return meter;
    }


    /* Convert from meters to feet */
    public static double meterToFoot(double meter){
        double foot;
        foot = (3.279 * meter);
        return foot;
    }
}

